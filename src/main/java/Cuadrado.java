public class Cuadrado {
    public static void main(String[] args) {
        int n = 4;  // Cambia esto por el n√∫mero de elementos por lado que quieras
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                if (i == 0 || i == n - 1 || j == 0 || j == n - 1) {
                    System.out.print("* ");
                } else {
                    System.out.print("  ");
                }
            }
            System.out.println();
        }
    }
}

